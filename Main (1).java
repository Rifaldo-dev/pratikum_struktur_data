public class Main {
  public static void main(String[] args) {
    byte umurByte = 25;       // Tipe byte
    short umurShort = 30000;  // Tipe short
    int umur = 25;            // Tipe int
    long populasi = 8000000000L; // Tipe long 
    float tinggi = 5.9f;      // Tipe float 
    double berat = 65.5;      // Tipe double
    char inisial = 'A';       // Tipe char
    boolean aktif = true;     // Tipe boolean
    

      // Output hasil
      System.out.println("ini tipe data byte        : " + umurByte);
      System.out.println("ini tipe data short    : " + umurShort);
      System.out.println("tipe data int               : " + umur + " tahun");
      System.out.println("tipe data long       : " + populasi + " orang");
      System.out.println("tipe data float       : " + tinggi + " cm");
      System.out.println("tipe data double      : " + berat + "kg");
      System.out.println("tipe data char      : " + inisial);
      System.out.println("tipe data bolean: " + aktif);
  }
}
